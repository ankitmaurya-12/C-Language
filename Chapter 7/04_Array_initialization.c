#include<stdio.h>

int main(){
    int a[] ={56,95,75,75};
    printf("The value of a[0] is %d\n", a[0]);
    printf("The value of a[1] is %d\n", a[1]);
    printf("The value of a[2] is %d\n", a[2]);
    printf("The value of a[3] is %d\n", a[3]);

    float b[] ={5.6,9.5,7.5,7.5};
    printf("The value of b[0] is %0.2f\n", b[0]);
    printf("The value of b[1] is %0.2f\n", b[1]);
    printf("The value of b[2] is %0.2f\n", b[2]);
    return 0;
}